SELECT DISTINCT ALUNO.*
FROM ALUNO
JOIN DISCIPLINA_ALUNO ON ALUNO.codigo_aluno = DISCIPLINA_ALUNO.codigo_aluno
JOIN DISCIPLINA ON DISCIPLINA_ALUNO.codigo_disciplina = DISCIPLINA.codigo_disciplina
WHERE YEAR(DISCIPLINA_ANO) BETWEEN 2000 AND 2020;




